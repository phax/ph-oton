package com.helger.html.hc.html;

import javax.annotation.Nonnull;
import javax.annotation.Nullable;

import com.helger.commons.annotation.Nonempty;
import com.helger.commons.lang.EnumHelper;
import com.helger.commons.string.StringHelper;

/**
 * Draggable?
 *
 * @author Philip Helger
 */
public enum EHCDraggable implements IHCHasHTMLAttributeValue
{
  TRUE ("true"),
  FALSE ("false"),
  AUTO ("auto");

  private final String m_sAttrValue;

  private EHCDraggable (@Nonnull @Nonempty final String sAttrValue)
  {
    m_sAttrValue = sAttrValue;
  }

  @Nonnull
  @Nonempty
  public String getAttrValue ()
  {
    return m_sAttrValue;
  }

  @Nullable
  public static EHCDraggable getFromAttrValueOrNull (@Nullable final String sAttrValue)
  {
    if (StringHelper.hasNoText (sAttrValue))
      return null;
    return EnumHelper.findFirst (EHCDraggable.class, x -> x.getAttrValue ().equals (sAttrValue));
  }
}
